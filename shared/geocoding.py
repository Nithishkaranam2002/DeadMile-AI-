def geocode_city(city: str, state: str) -> tuple[float, float]:
    """Look up coordinates for a city/state pair."""
    city = city.strip()
    state = state.strip().upper()
    key = f"{city}, {state}"
    if key in CITY_COORDINATES:
        return CITY_COORDINATES[key]
    # Try without normalizing spaces in multi-word cities
    for known_key, coords in CITY_COORDINATES.items():
        known_city, known_state = known_key.rsplit(", ", 1)
        if known_state == state and known_city.lower() == city.lower():
            return coords
    if state in STATE_CENTROIDS:
        return STATE_CENTROIDS[state]
    return (39.8283, -98.5795)  # geographic center of contiguous US
